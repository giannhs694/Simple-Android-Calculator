public class Numbers {
    private double left;
    private double right;

    public Numbers() {
        this.left = left;
        this.right = right;
    }

    public Numbers(double left,double right) {
        this.left = left;
        this.right = right;
    }

    public double getRight() {
        return right;
    }


    public double getLeft() {
        return left;
    }

    public void setRight(double right) {
        this.right = right;
    }

    public void setLeft(double left) {
        this.left = left;
    }
}
